def total_score(score, multiplier):
    return score * multiplier

# The function call 'total_score()' calls the function and is printed by print()
print(total_score(3000,1.5))# 1ai. 3000 --> Score | 1.5 --> Multiplier
print(total_score(4000,2))

# 1bi. Output:  4500.0 --> Float b/c of decimal multiplier
